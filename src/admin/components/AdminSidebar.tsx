export function AdminSidebar() {
  const [pendingCount, setPendingCount] = useState(0);

  useEffect(() => {
    async function fetchPendingCount() {
      const { count } = await supabase
        .from('trips')
        .select('*', { count: 'exact', head: true })
        .eq('status', 'submitted');
      setPendingCount(count ?? 0);
    }
    fetchPendingCount();
  }, []);

  return (
    <aside className="w-64 bg-white shadow-sm min-h-screen">
      <nav className="mt-5 px-2">
        <div className="space-y-1">
          {navigation.map((item) => (
            <NavLink
              key={item.name}
              to={item.path}
              className={({ isActive }) =>
                `group flex items-center px-2 py-2 text-sm font-medium rounded-md ${isActive
                  ? 'bg-gray-100 text-gray-900'
                  : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                }`
              }
            >
              <div className="mr-3 flex-shrink-0">{item.icon}</div>
              <span className="flex-1">{item.name}</span>
              {item.name === 'Quotes' && pendingCount > 0 && (
                <span className="ml-auto inline-flex h-5 min-w-[1.25rem] items-center justify-center rounded-full bg-red-500 px-1.5 text-xs font-bold text-white">
                  {pendingCount}
                </span>
              )}
            </NavLink>
          ))}
        </div>
      </nav>
    </aside>
  );
} 